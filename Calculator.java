    private String CaculatorClient(String accumulated, String text, String currentOperator) {
        try {
            switch (currentOperator) {
                case "+":
                    return c.AddRemote(Double.parseDouble(accumulated), Double.parseDouble(text)) + "";
                case "-":
                    return c.SubRemote(Double.parseDouble(accumulated), Double.parseDouble(text)) + "";
                case "/":
                    return c.DivRemote(Double.parseDouble(accumulated), Double.parseDouble(text)) + "";
                case "*":
                    return c.MulRemote(Double.parseDouble(accumulated), Double.parseDouble(text)) + "";
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
        return "";
    }
