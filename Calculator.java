    private void setText(String text) {
        if (isActive) {
            isActive = !isActive;
            resultTextView.setText(text);
        } else if (resultTextView.getText().equals("0")) {
            resultTextView.setText(text);
        } else {
            resultTextView.setText(resultTextView.getText() + text);
        }
    }

    private String currentOperator = "";
    private String accumulated = "";
    private boolean isActive = false;

    private void adminOperator(String operator) {
        if (currentOperator.equals("")) {
            isActive = true;
            currentOperator = operator;
            accumulated = resultTextView.getText();
            return;
        }
        String result = CaculatorClient(accumulated, resultTextView.getText(), currentOperator);
        accumulated = result;
        resultTextView.setText(result);
        currentOperator = operator;
        isActive = true;
    }
